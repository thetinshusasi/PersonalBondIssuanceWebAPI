        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, ERC20BurnableDeployment eRC20BurnableDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<ERC20BurnableDeployment>().SendRequestAndWaitForReceiptAsync(eRC20BurnableDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, ERC20BurnableDeployment eRC20BurnableDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<ERC20BurnableDeployment>().SendRequestAsync(eRC20BurnableDeployment);
        }

        public static async Task<ERC20BurnableService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, ERC20BurnableDeployment eRC20BurnableDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, eRC20BurnableDeployment, cancellationTokenSource);
            return new ERC20BurnableService(web3, receipt.ContractAddress);
        }

